CREATE TABLE users (
    id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    email    text                                    NOT NULL,
    login    text                                    NOT NULL,
    name     text,
    birthday timestamp with time zone                NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE friendship (
    requesting_user_id bigint NOT NULL,
    receiving_user_id  bigint NOT NULL,
    PRIMARY KEY (requesting_user_id, receiving_user_id),
    FOREIGN KEY (requesting_user_id) REFERENCES users (id) ON DELETE CASCADE,
    FOREIGN KEY (receiving_user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE mpa (
    id   bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name text                                    NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE genre (
    id   bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name text                                    NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE director (
    id   bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name text                                    NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE film (
    id           bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name         text                                    NOT NULL,
    description  text                                    NOT NULL,
    release_date timestamp with time zone                NOT NULL,
    duration     integer                                 NOT NULL,
    mpa_id       bigint                                  NOT NULL,
    likes_amount integer                                 NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (mpa_id) REFERENCES mpa (id)
);

CREATE TABLE film_genre (
    film_id  bigint NOT NULL,
    genre_id bigint NOT NULL,
    PRIMARY KEY (film_id, genre_id),
    FOREIGN KEY (film_id) REFERENCES film (id) ON DELETE CASCADE,
    FOREIGN KEY (genre_id) REFERENCES genre (id) ON DELETE CASCADE
);

CREATE TABLE film_director (
    film_id     bigint NOT NULL,
    director_id bigint NOT NULL,
    PRIMARY KEY (film_id, director_id),
    FOREIGN KEY (film_id) REFERENCES film (id) ON DELETE CASCADE,
    FOREIGN KEY (director_id) REFERENCES director (id) ON DELETE CASCADE
);

CREATE TABLE film_like (
    film_id bigint NOT NULL,
    user_id bigint NOT NULL,
    PRIMARY KEY (film_id, user_id),
    FOREIGN KEY (film_id) REFERENCES film (id) ON DELETE CASCADE,
    FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE event (
    id         bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created    timestamp with time zone                NOT NULL,
    user_id    bigint                                  NOT NULL,
    event_type text                                    NOT NULL,
    operation  text                                    NOT NULL,
    entity_id  bigint                                  NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE review (
    id          bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    content     text                                    NOT NULL,
    is_positive boolean                                 NOT NULL,
    user_id     bigint                                  NOT NULL,
    film_id     bigint                                  NOT NULL,
    useful      integer                                 NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    FOREIGN KEY (film_id) REFERENCES film (id) ON DELETE CASCADE
);

CREATE TABLE review_mark (
    review_id bigint NOT NULL,
    user_id   bigint NOT NULL,
    mark_type text   NOT NULL,
    PRIMARY KEY (review_id, user_id),
    FOREIGN KEY (review_id) REFERENCES review (id) ON DELETE CASCADE,
    FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

